        "       - Логин и пароль от hh.ru (хранятся в зашифрованном виде)\n"
        "       - Текст резюме (будет использоваться в откликах)\n"
        "       - URL фильтра поиска (например, ссылка на поиск с параметрами)\n"
        "       - Прокси (если необходимо)\n\n"
        "2. <b>Парсинг вакансий</b>\n"
        "   • Кнопка «🔍 Парсинг вакансий по фильтру» запускает автоматический поиск новых вакансий согласно вашему фильтру. "
        "Найденные вакансии сохраняются в базу данных и будут использоваться для откликов.\n"
        "   • Кнопка «📝 Парсинг вакансии и генерация сопроводительного» позволяет вручную ввести ссылку на конкретную вакансию, "
