from aiogram import types, Router, F
from aiogram.filters import Command
from aiogram.fsm.context import FSMContext

from app.config import settings
from app.keyboards.reply import get_main_keyboard

router = Router()


@router.message(Command("start"))
async def cmd_start(message: types.Message, state: FSMContext):
    uid = message.from_user.id
    # reply = get_main_keyboard(message.from_user.id) if uid != settings.ADMIN_ID else
    await state.clear()
    await message.answer(
        "Привет! Я бот для упрощения поиска работы.\n"
        "Выберите действие на клавиатуре ниже:",
        reply_markup=get_main_keyboard(uid)
    )


@router.message(Command("help"))
async def cmd_help(message: types.Message, state: FSMContext):
    """Краткая справка по боту."""
    user_id = message.from_user.id
    is_admin = (user_id == settings.ADMIN_ID)

    text = (
        "🤖 <b>HeadHunter Бот – краткая справка</b>\n\n"
        "Бот помогает искать вакансии на hh.ru и автоматически откликаться на них.\n\n"
        "<b>Основные возможности:</b>\n"
        "• Парсинг вакансий по вашему фильтру (кнопка «🔍 Парсинг вакансий по фильтру»)\n"
        "• Ручной парсинг одной вакансии и генерация сопроводительного письма (кнопка «📝 Парсинг вакансии и генерация…»)\n"
        "• Просмотр статистики откликов (кнопка «📊 Статистика»)\n"
        "• Просмотр всех данных аккаунта (кнопка «📋 Все данные»)\n"
        "• Тестовый режим для проверки генерации писем (кнопка «🧪 Тестовый режим»)\n"
        "• Настройка аккаунта: логин/пароль, прокси, фильтр, резюме (кнопка «⚙️ Настройки аккаунта»)\n\n"
    )

    if is_admin:
        text += (
            "<b>Для администратора:</b>\n"
            "• Кнопка «👑 Админ-панель» – управление аккаунтами, просмотр статистики, загрузка cookies и др.\n"
        )
    else:
        text += "Для получения дополнительной информации обратитесь к администратору.\n"

    text += (
        "\nПодробная инструкция доступна по команде /instructions"
    )

    await message.answer(text, reply_markup=get_main_keyboard(user_id), parse_mode="HTML")


